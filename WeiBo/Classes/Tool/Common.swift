import Foundation
import UIKit

func CLYfont(number:CGFloat)->UIFont{
  return UIFont.systemFont(ofSize: number)
}

func CLYBoldfont(number:CGFloat)->UIFont{
    return UIFont.boldSystemFont(ofSize: number)
}

